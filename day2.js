var x=10, y=20;
console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);   
console.log(x%y);  //modulus operator gives the remainder of the division
console.log(x**y); //exponentiation operator gives the result of x raised to the power of y 
console.log(x++); //post-increment operator returns the value of x before incrementing it
console.log(x); 
console.log(++y); //pre-increment operator increments y before returning its value
console.log(y); 